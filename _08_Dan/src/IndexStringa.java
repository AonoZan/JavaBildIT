public class IndexStringa {
	public static void main(String[] args) {
		String s = "DOBRODOSLI" + "asd";
		
		System.out.println("Duzina stringa je " + s.length());
		System.out.println("Karakter na indeksu 0 je " + s.charAt(0));
		System.out.println("Karakter na poslednjem indeksu je " + s.charAt(s.length() - 1));
		
	}

}
