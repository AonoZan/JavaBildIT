public class _8_AreaAndPerimeterOfCircle {

	public static void main(String[] args) {
		double radius = 5.5;
		double area = 2 * radius * Math.PI;
		double perimeter = radius * radius * Math.PI;
		System.out.printf("Circle that has radius of %.2f,\n have area of %.2f and perimeter of %.2f.", radius, area, perimeter);
	}

}
