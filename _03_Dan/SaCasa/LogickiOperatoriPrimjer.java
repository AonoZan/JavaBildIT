package NaCasuPisano;
public class LogickiOperatoriPrimjer {

	public static void main(String[] args) {
		int i = 6;
		
		if ((i <10) && (i > 5)) {
			System.out.println("i je manje od 10 a vece od 5");
		}
		
		if ((i <10) || (i > 5)) {
			System.out.println( "i je ili manje od 10 ili vece od 5");
		}
		
	}

}
